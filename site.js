const year = document.querySelector("#year");
const inquiryForm = document.querySelector("#inquiryForm");
const availabilityFilter = document.querySelector("#availabilityFilter");
const propertyGrid = document.querySelector("#propertyGrid");
const availabilityStatus = document.querySelector("#availabilityStatus");
const resetAvailability = document.querySelector("#resetAvailability");
const languageButtons = document.querySelectorAll("[data-lang]");

let currentLanguage = localStorage.getItem("ebrostay-language") || "es";
let activeFilter = null;
let availabilityMessageOverride = null;

const t = (key) => translations[currentLanguage][key] || translations.es[key] || key;

function interpolate(key, values) {
  return Object.entries(values).reduce((text, [name, value]) => {
    return text.replaceAll(`{${name}}`, value);
  }, t(key));
}

function dateValue(value) {
  return value ? new Date(`${value}T00:00:00`) : null;
}

function rangesOverlap(startA, endA, startB, endB) {
  return startA < endB && startB < endA;
}

function isAvailable(property, filter) {
  if (!filter) {
    return true;
  }

  if (property.guests < filter.guests) {
    return false;
  }

  return !property.unavailable.some(([start, end]) => {
    return rangesOverlap(filter.checkIn, filter.checkOut, dateValue(start), dateValue(end));
  });
}

function renderProperties() {
  if (!propertyGrid || !availabilityStatus) {
    return;
  }

  const rendered = properties.map((property) => {
    const available = isAvailable(property, activeFilter);
    return { ...property, available };
  });

  const visible = activeFilter ? rendered.filter((property) => property.available) : rendered;
  const count = visible.length;

  if (availabilityMessageOverride) {
    availabilityStatus.textContent = availabilityMessageOverride;
  } else if (!activeFilter) {
    availabilityStatus.textContent = t("availability.all");
  } else if (count === 0) {
    availabilityStatus.textContent = t("availability.none");
  } else if (count === 1) {
    availabilityStatus.textContent = t("availability.oneMatch");
  } else {
    availabilityStatus.textContent = interpolate("availability.matches", { count });
  }

  propertyGrid.innerHTML = visible.map((property) => {
    const pillClass = property.available ? "is-open" : "is-blocked";
    const pillText = property.available ? t("availability.open") : t("availability.blocked");

    return `
      <article class="property-card${property.available ? "" : " is-unavailable"}">
        <span class="availability-pill ${pillClass}">${pillText}</span>
        <div>
          <h3>${t(property.nameKey)}</h3>
          <p>${t(property.copyKey)}</p>
        </div>
        <div class="property-meta">
          <span>${t(property.areaKey)}</span>
          <span>${interpolate("availability.capacity", { guests: property.guests })}</span>
          <span>${interpolate("availability.from", { price: property.price })}</span>
        </div>
      </article>
    `;
  }).join("");
}

function applyLanguage(language) {
  currentLanguage = translations[language] ? language : "es";
  localStorage.setItem("ebrostay-language", currentLanguage);
  document.documentElement.lang = currentLanguage;
  document.title = t("meta.title");

  document.querySelectorAll("[data-i18n]").forEach((element) => {
    element.textContent = t(element.dataset.i18n);
  });

  document.querySelectorAll("[data-i18n-attr]").forEach((element) => {
    element.dataset.i18nAttr.split(";").forEach((pair) => {
      const [attribute, key] = pair.split(":");
      if (attribute && key) {
        element.setAttribute(attribute, t(key));
      }
    });
  });

  languageButtons.forEach((button) => {
    button.classList.toggle("is-active", button.dataset.lang === currentLanguage);
    button.setAttribute("aria-pressed", String(button.dataset.lang === currentLanguage));
  });

  renderProperties();
}

if (year) {
  year.textContent = new Date().getFullYear();
}

languageButtons.forEach((button) => {
  button.addEventListener("click", () => applyLanguage(button.dataset.lang));
});

if (availabilityFilter) {
  availabilityFilter.addEventListener("submit", (event) => {
    event.preventDefault();
    const formData = new FormData(availabilityFilter);
    const checkIn = dateValue(formData.get("checkIn"));
    const checkOut = dateValue(formData.get("checkOut"));
    const guests = Number(formData.get("guestCount")) || 1;

    if (!checkIn || !checkOut || checkOut <= checkIn) {
      activeFilter = null;
      availabilityMessageOverride = t("availability.invalid");
      renderProperties();
      return;
    }

    availabilityMessageOverride = null;
    activeFilter = { checkIn, checkOut, guests };
    renderProperties();
  });
}

if (resetAvailability) {
  resetAvailability.addEventListener("click", () => {
    activeFilter = null;
    availabilityMessageOverride = null;
    availabilityFilter?.reset();
    renderProperties();
  });
}

if (inquiryForm) {
  inquiryForm.addEventListener("submit", (event) => {
    event.preventDefault();

    const formData = new FormData(inquiryForm);
    const name = formData.get("name")?.toString().trim() || "Guest";
    const email = formData.get("email")?.toString().trim() || "";
    const property = formData.get("property")?.toString().trim() || t("email.propertyFallback");
    const message = formData.get("message")?.toString().trim() || t("email.defaultMessage");

    const subject = encodeURIComponent(`${t("email.subject")} ${name}`);
    const body = encodeURIComponent(
      `Name / Nombre: ${name}\nEmail: ${email}\nProperty / Propiedad: ${property}\n\n${message}`
    );

    window.location.href = `mailto:hello@ebrostay.com?subject=${subject}&body=${body}`;
  });
}

applyLanguage(currentLanguage);
